def choose_unique_base_stem(orig_stem, img_ext, lbl_exts_present, src_tag):
    """
    Find a unique base stem such that:
    - {TARGET_IMAGES}/{stem}{img_ext} doesn't exist, and
    - {TARGET_LABELS}/{stem}{each label ext present} doesn't exist.
    Strategy: try plain stem, then <src_tag>_<stem>, then add _1, _2, ...
    Returns the chosen base stem string.
    """
    # 1) try plain stem
    if all_targets_free(orig_stem, img_ext, lbl_exts_present):
        return orig_stem

