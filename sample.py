# Map YOLO class IDs to names (fill if you know them; unknowns will be "unknown")
CLASS_MAPPING = {
    # 0: 'Three_Wheeler',
    # 1: 'Two_Wheeler',
    # 2: 'Four_Wheeler',
    # ...
}
# =====================

def prettify_xml(elem) -> str:
    rough = ET.tostring(elem, 'utf-8')
    return minidom.parseString(rough).toprettyxml(indent="    ")

def clamp(v, lo, hi):
    return max(lo, min(hi, v))

def find_image_for_label(stem: str):
    for ext in ('.jpg', '.jpeg', '.png', '.JPG', '.JPEG', '.PNG'):
        p = os.path.join(IMAGES_DIR, stem + ext)
        if os.path.exists(p):
            return p
    return None

def yolo_line_to_bbox(abs_w, abs_h, x_c, y_c, w, h):
    x_min = int(round((x_c - w/2) * abs_w))
    y_min = int(round((y_c - h/2) * abs_h))
    x_max = int(round((x_c + w/2) * abs_w))
    y_max = int(round((y_c + h/2) * abs_h))

    x_min = clamp(x_min, 0, abs_w - 1)
    y_min = clamp(y_min, 0, abs_h - 1)
    x_max = clamp(x_max, 0, abs_w - 1)
    y_max = clamp(y_max, 0, abs_h - 1)
    return x_min, y_min, x_max, y_max

def convert_one_txt(txt_path: str):
    stem = os.path.splitext(os.path.basename(txt_path))[0]
    img_path = find_image_for_label(stem)
    if not img_path:
        print(f"[WARN] No image for: {txt_path}")
        return

    try:
        with Image.open(img_path) as im:
            w, h = im.size
            depth = len(im.getbands())
    except Exception as e:
        print(f"[ERROR] Open image failed ({img_path}): {e}")
        return

    ann = ET.Element('annotation')
    ET.SubElement(ann, 'folder').text = os.path.basename(os.path.dirname(img_path))
    ET.SubElement(ann, 'filename').text = os.path.basename(img_path)

    size = ET.SubElement(ann, 'size')
    ET.SubElement(size, 'width').text  = str(w)
    ET.SubElement(size, 'height').text = str(h)
    ET.SubElement(size, 'depth').text  = str(depth)

    with open(txt_path, 'r') as f:
        for raw in f:
            line = raw.strip()
            if not line:
                continue
            parts = line.split()
            if len(parts) != 5:
                # malformed line; skip
                continue

            try:
                cls_id = int(parts[0])
                x_c, y_c, bw, bh = map(float, parts[1:])
            except ValueError:
                continue

            xmin, ymin, xmax, ymax = yolo_line_to_bbox(w, h, x_c, y_c, bw, bh)
            if xmax <= xmin or ymax <= ymin:
                # degenerate box; skip
                continue

            obj = ET.SubElement(ann, 'object')
            ET.SubElement(obj, 'name').text = CLASS_MAPPING.get(cls_id, 'unknown')
            ET.SubElement(obj, 'pose').text = 'Unspecified'
            ET.SubElement(obj, 'truncated').text = '0'
            ET.SubElement(obj, 'difficult').text = '0'

            bnd = ET.SubElement(obj, 'bndbox')
            ET.SubElement(bnd, 'xmin').text = str(xmin)
            ET.SubElement(bnd, 'ymin').text = str(ymin)
            ET.SubElement(bnd, 'xmax').text = str(xmax)
            ET.SubElement(bnd, 'ymax').text = str(ymax)

    xml_out = os.path.join(LABELS_DIR, stem + '.xml')
    with open(xml_out, 'w') as xf:
        xf.write(prettify_xml(ann))
    print(f"[OK] {os.path.basename(txt_path)} → {os.path.basename(xml_out)}")

def main():
    if not os.path.isdir(LABELS_DIR):
        raise FileNotFoundError(f"Labels dir not found: {LABELS_DIR}")
    if not os.path.isdir(IMAGES_DIR):
        print(f"[WARN] Images dir not found: {IMAGES_DIR} (image lookup will fail)")

    txt_files = [f for f in os.listdir(LABELS_DIR) if f.lower().endswith('.txt')]
    if not txt_files:
        print("[INFO] No .txt files found.")
        return

    for fname in sorted(txt_files):
        convert_one_txt(os.path.join(LABELS_DIR, fname))

    print("\n✅ Done converting all .txt to .xml in:", LABELS_DIR)

if __name__ == "__main__":
    main()

