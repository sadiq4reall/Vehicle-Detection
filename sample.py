# Our dataset has 2 classes: 1 (car) + 0 (background)
num_classes = 5  # 4 vehicle classes + 1 background

# Load a pre-trained Faster R-CNN model
model = torchvision.models.detection.fasterrcnn_resnet50_fpn(weights='DEFAULT')

# Get the number of input features for the classifier
in_features = model.roi_heads.box_predictor.cls_score.in_features

# Replace the pre-trained head with a new one for our number of classes
model.roi_heads.box_predictor = torchvision.models.detection.faster_rcnn.FastRCNNPredictor(in_features, num_classes)

# Move model to the appropriate device
model.to(device)

# Use DataParallel if multiple GPUs are available
if torch.cuda.device_count() > 1:
    model = torch.nn.DataParallel(model)
    print(f"Using {torch.cuda.device_count()} GPUs!")

# --- Training Setup ---
# Define optimizer and learning rate scheduler
params = [p for p in model.parameters() if p.requires_grad]
optimizer = torch.optim.SGD(params, lr=0.005, momentum=0.9, weight_decay=0.0005)
lr_scheduler = torch.optim.lr_scheduler.StepLR(optimizer, step_size=3, gamma=0.1)

num_epochs = 3 if not torch.cuda.is_available() else 10
print(f"--- Faster R-CNN Training Configuration: {num_epochs} Epochs ---")

# --- Training Loop ---
for epoch in range(num_epochs):
    model.train()  # Set the model to training mode
    print(f"--- Epoch {epoch+1}/{num_epochs} ---")

    epoch_loss = 0
    for images, targets in tqdm(data_loader_train, desc=f"Training Epoch {epoch+1}"):
        # Move images and targets to the appropriate device (GPU or CPU)
        images = list(image.to(device) for image in images)
        targets = [{k: v.to(device) for k, v in t.items()} for t in targets]

        # Forward pass
        loss_dict = model(images, targets)

        # Total loss
        losses = sum(loss for loss in loss_dict.values())
        loss_value = losses.item()
        epoch_loss += loss_value

        # Backward pass and optimization
        optimizer.zero_grad()
        losses.backward()
        optimizer.step()

    # Step the learning rate scheduler
    lr_scheduler.step()

    # Print loss for the current epoch
    print(f"---- Epoch {epoch+1} finished. Average loss: {epoch_loss/len(data_loader_train):.4f} ----")

print("--- Training complete. ---")

