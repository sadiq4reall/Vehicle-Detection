classes = ["Four_Wheeler", "Large_Vehicle", "Three_Wheeler", "Two_Wheeler", "Bicycle"]

def voc_to_yolo_box(xmin, ymin, xmax, ymax, img_w, img_h):
    x_c = (xmin + xmax) / 2.0 / img_w
    y_c = (ymin + ymax) / 2.0 / img_h
    w   = (xmax - xmin) / img_w
    h   = (ymax - ymin) / img_h
    return x_c, y_c, w, h

converted = 0
for fname in sorted(os.listdir(xml_path)):
    if not fname.endswith(".xml"):
        continue

    xml_file = os.path.join(xml_path, fname)
    tree = ET.parse(xml_file)
    root = tree.getroot()

    size = root.find("size")
    img_w = int(size.find("width").text)
    img_h = int(size.find("height").text)

    out_txt = os.path.join(txt_path, fname.replace(".xml", ".txt"))
    with open(out_txt, "w") as f:
        for obj in root.findall("object"):
            cls = obj.find("name").text.strip()
            if cls not in classes:
                continue
            cls_id = classes.index(cls)

            bb = obj.find("bndbox")
            xmin, ymin = int(bb.find("xmin").text), int(bb.find("ymin").text)
            xmax, ymax = int(bb.find("xmax").text), int(bb.find("ymax").text)

            x_c, y_c, w, h = voc_to_yolo_box(xmin, ymin, xmax, ymax, img_w, img_h)
            f.write(f"{cls_id} {x_c:.6f} {y_c:.6f} {w:.6f} {h:.6f}\n")

